     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $noRekening = $request->input('no_rek');

        $datarekening = ModelRekening::where('No_Rekening', $noRekening)->first();

        if($datarekening) {
            return Redirect::to('/user/data/'. base64_encode($noRekening));
        }
        else {
            return Redirect::to('/user/login/')->with('login-error', 'Nomor rekening anda tidak ditemukan');
        }
    }

